int igual(int x, int y) {
    if (x == y) { 
        return 1;
    } else {
        return 0;
    }
}

int maior(int x, int y) {
    if (x > y) {
        return x;
    } else {
        return y;
    }
}

int menor(int x, int y) {
    if (x < y) {
        return x;
    } else {
        return y;
    }
}